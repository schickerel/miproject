/**
 * Base class that represents a query for the 'migrations' table.
 *
 *
 *
 * @method MigrationQuery orderById($order = Criteria::ASC) Order by the id column
 * @method MigrationQuery orderByCity($order = Criteria::ASC) Order by the city column
 * @method MigrationQuery orderByCountryId($order = Criteria::ASC) Order by the country_id column
 * @method MigrationQuery orderByMonth($order = Criteria::ASC) Order by the month column
 * @method MigrationQuery orderByYear($order = Criteria::ASC) Order by the year column
 * @method MigrationQuery orderByPersonId($order = Criteria::ASC) Order by the person_id column
 *
 * @method MigrationQuery groupById() Group by the id column
 * @method MigrationQuery groupByCity() Group by the city column
 * @method MigrationQuery groupByCountryId() Group by the country_id column
 * @method MigrationQuery groupByMonth() Group by the month column
 * @method MigrationQuery groupByYear() Group by the year column
 * @method MigrationQuery groupByPersonId() Group by the person_id column
 *
 * @method MigrationQuery leftJoin($relation) Adds a LEFT JOIN clause to the query
 * @method MigrationQuery rightJoin($relation) Adds a RIGHT JOIN clause to the query
 * @method MigrationQuery innerJoin($relation) Adds a INNER JOIN clause to the query
 *
 * @method MigrationQuery leftJoinCountry($relationAlias = null) Adds a LEFT JOIN clause to the query using the Country relation
 * @method MigrationQuery rightJoinCountry($relationAlias = null) Adds a RIGHT JOIN clause to the query using the Country relation
 * @method MigrationQuery innerJoinCountry($relationAlias = null) Adds a INNER JOIN clause to the query using the Country relation
 *
 * @method MigrationQuery leftJoinPerson($relationAlias = null) Adds a LEFT JOIN clause to the query using the Person relation
 * @method MigrationQuery rightJoinPerson($relationAlias = null) Adds a RIGHT JOIN clause to the query using the Person relation
 * @method MigrationQuery innerJoinPerson($relationAlias = null) Adds a INNER JOIN clause to the query using the Person relation
 *
 * @method Migration findOne(PropelPDO $con = null) Return the first Migration matching the query
 * @method Migration findOneOrCreate(PropelPDO $con = null) Return the first Migration matching the query, or a new Migration object populated from the query conditions when no match is found
 *
 * @method Migration findOneByCity(string $city) Return the first Migration filtered by the city column
 * @method Migration findOneByCountryId(int $country_id) Return the first Migration filtered by the country_id column
 * @method Migration findOneByMonth(int $month) Return the first Migration filtered by the month column
 * @method Migration findOneByYear(int $year) Return the first Migration filtered by the year column
 * @method Migration findOneByPersonId(int $person_id) Return the first Migration filtered by the person_id column
 *
 * @method array findById(int $id) Return Migration objects filtered by the id column
 * @method array findByCity(string $city) Return Migration objects filtered by the city column
 * @method array findByCountryId(int $country_id) Return Migration objects filtered by the country_id column
 * @method array findByMonth(int $month) Return Migration objects filtered by the month column
 * @method array findByYear(int $year) Return Migration objects filtered by the year column
 * @method array findByPersonId(int $person_id) Return Migration objects filtered by the person_id column
 *
 * @package    propel.generator.Migration.om
 */
abstract class BaseMigrationQuery extends ModelCriteria
{
    /**
     * Initializes internal state of BaseMigrationQuery object.
     *
     * @param     string $dbName The dabase name
     * @param     string $modelName The phpName of a model, e.g. 'Book'
     * @param     string $modelAlias The alias for the model in this query, e.g. 'b'
     */
    public function __construct($dbName = null, $modelName = null, $modelAlias = null)
    {
        if (null === $dbName) {
            $dbName = 'migrationstreams';
        }
        if (null === $modelName) {
            $modelName = 'Migration\\Migration';
        }
        parent::__construct($dbName, $modelName, $modelAlias);
    }

    /**
     * Returns a new MigrationQuery object.
     *
     * @param     string $modelAlias The alias of a model in the query
     * @param   MigrationQuery|Criteria $criteria Optional Criteria to build the query from
     *
     * @return MigrationQuery
     */
    public static function create($modelAlias = null, $criteria = null)
    {
        if ($criteria instanceof MigrationQuery) {
            return $criteria;
        }
        $query = new MigrationQuery(null, null, $modelAlias);

        if ($criteria instanceof Criteria) {
            $query->mergeWith($criteria);
        }

        return $query;
    }

    /**
     * Find object by primary key.
     * Propel uses the instance pool to skip the database if the object exists.
     * Go fast if the query is untouched.
     *
     * <code>
     * $obj  = $c->findPk(12, $con);
     * </code>
     *
     * @param mixed $key Primary key to use for the query
     * @param     PropelPDO $con an optional connection object
     *
     * @return   Migration|Migration[]|mixed the result, formatted by the current formatter
     */
    public function findPk($key, $con = null)
    {
        if ($key === null) {
            return null;
        }
        if ((null !== ($obj = MigrationPeer::getInstanceFromPool((string) $key))) && !$this->formatter) {
            // the object is already in the instance pool
            return $obj;
        }
        if ($con === null) {
            $con = Propel::getConnection(MigrationPeer::DATABASE_NAME, Propel::CONNECTION_READ);
        }
        $this->basePreSelect($con);
        if ($this->formatter || $this->modelAlias || $this->with || $this->select
         || $this->selectColumns || $this->asColumns || $this->selectModifiers
         || $this->map || $this->having || $this->joins) {
            return $this->findPkComplex($key, $con);
        } else {
            return $this->findPkSimple($key, $con);
        }
    }

    /**
     * Alias of findPk to use instance pooling
     *
     * @param     mixed $key Primary key to use for the query
     * @param     PropelPDO $con A connection object
     *
     * @return                 Migration A model object, or null if the key is not found
     * @throws PropelException
     */
     public function findOneById($key, $con = null)
     {
        return $this->findPk($key, $con);
     }

    /**
     * Find object by primary key using raw SQL to go fast.
     * Bypass doSelect() and the object formatter by using generated code.
     *
     * @param     mixed $key Primary key to use for the query
     * @param     PropelPDO $con A connection object
     *
     * @return                 Migration A model object, or null if the key is not found
     * @throws PropelException
     */
    protected function findPkSimple($key, $con)
    {
        $sql = 'SELECT `id`, `city`, `country_id`, `month`, `year`, `person_id` FROM `migrations` WHERE `id` = :p0';
        try {
            $stmt = $con->prepare($sql);
            $stmt->bindValue(':p0', $key, PDO::PARAM_INT);
            $stmt->execute();
        } catch (Exception $e) {
            Propel::log($e->getMessage(), Propel::LOG_ERR);
            throw new PropelException(sprintf('Unable to execute SELECT statement [%s]', $sql), $e);
        }
        $obj = null;
        if ($row = $stmt->fetch(PDO::FETCH_NUM)) {
            $obj = new Migration();
            $obj->hydrate($row);
            MigrationPeer::addInstanceToPool($obj, (string) $key);
        }
        $stmt->closeCursor();

        return $obj;
    }

    /**
     * Find object by primary key.
     *
     * @param     mixed $key Primary key to use for the query
     * @param     PropelPDO $con A connection object
     *
     * @return Migration|Migration[]|mixed the result, formatted by the current formatter
     */
    protected function findPkComplex($key, $con)
    {
        // As the query uses a PK condition, no limit(1) is necessary.
        $criteria = $this->isKeepQuery() ? clone $this : $this;
        $stmt = $criteria
            ->filterByPrimaryKey($key)
            ->doSelect($con);

        return $criteria->getFormatter()->init($criteria)->formatOne($stmt);
    }

    /**
     * Find objects by primary key
     * <code>
     * $objs = $c->findPks(array(12, 56, 832), $con);
     * </code>
     * @param     array $keys Primary keys to use for the query
     * @param     PropelPDO $con an optional connection object
     *
     * @return PropelObjectCollection|Migration[]|mixed the list of results, formatted by the current formatter
     */
    public function findPks($keys, $con = null)
    {
        if ($con === null) {
            $con = Propel::getConnection($this->getDbName(), Propel::CONNECTION_READ);
        }
        $this->basePreSelect($con);
        $criteria = $this->isKeepQuery() ? clone $this : $this;
        $stmt = $criteria
            ->filterByPrimaryKeys($keys)
            ->doSelect($con);

        return $criteria->getFormatter()->init($criteria)->format($stmt);
    }

    /**
     * Filter the query by primary key
     *
     * @param     mixed $key Primary key to use for the query
     *
     * @return MigrationQuery The current query, for fluid interface
     */
    public function filterByPrimaryKey($key)
    {

        return $this->addUsingAlias(MigrationPeer::ID, $key, Criteria::EQUAL);
    }

